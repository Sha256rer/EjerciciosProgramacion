package EstructurasControl;

public class TodasLasTablas {
    public static void main(String[] args) {
        int mult;
        for (int i = 0; i <11 ; i++) {
            for (int j = 0; j < 11; j++) {
                mult = i*j;
                System.out.println(i+" x "+ j+" = "+mult);

            }


        }
    }
}
